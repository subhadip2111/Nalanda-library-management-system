// Regex patterns for validation

// Email format validation
exports.emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

// Password length validation (at least 8 characters)
exports.passwordLengthRegex = /^.{8,}$/;


// Regular expression for valid ISBN format (example)
exports.isValidISBN = /^\d{13}$/;
// Regular expression for valid date format (YYYY-MM-DD)
exports.isValidDate = /^\d{4}-\d{2}-\d{2}$/;

